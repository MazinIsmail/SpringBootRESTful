package com.springRESTful.openAPI;

import org.springframework.stereotype.Component;

/**
 * OpenAPI Specification (OAS)
 * 
 * The OpenAPI Specification, originally known as the Swagger Specification, is
 * a specification for machine-readable interface files for describing,
 * producing, consuming, and visualizing RESTful web services. Originally part
 * of the Swagger framework, it became a separate project in 2016, overseen by
 * the OpenAPI Initiative, an open-source collaboration project of the Linux
 * Foundation. Swagger and some other tools can generate code, documentation and
 * test cases given an interface file.
 * 
 * Applications implemented based on OpenAPI interface files can automatically
 * generate documentation of methods, parameters and models. This helps keep the
 * documentation, client libraries, and source code in sync.
 * 
 * The OAS is language agnostic, and is both human and machine readable, which
 * allows both people and computers to discover and understand the capabilities
 * of a service without requiring access to source code, additional
 * documentation, or inspection of network traffic.
 * 
 * Swagger can handle both JSON and XML.
 * 
 * Swagger Codegen contains a template-driven engine to generate documentation,
 * API clients and server stubs in different languages by parsing the OpenAPI
 * definition.
 * 
 * OAS provides a common framework of designers, developers, testers, and devops
 * to build and maintain APIs.
 * 
 * OpenAPI-Driven API Development:-
 * 
 * Definition driven API development advocates for designing the API's
 * definition first before any other lifecycle operation has been implemented.
 * This means even before you start building the API's business logic, before
 * you test the API for any errors or defects, or any other lifecycle function,
 * you will design the API's interface, detailing the exact requests and
 * responses your API end points will showcase.
 * 
 * Benefits of an OAS-Driven Approach:-
 * 
 * Better Developer Experience (DX): DX is the aggregate of all the experiences
 * a developer may experience, both positive and negative, when consuming and
 * integrating with your software. Good developer experience for API consumption
 * is critical in a hyper competitive API ecosystem.
 * 
 * Enables Independence: The approach reduced dependencies between different
 * teams working on your API, like the front-end and back-end teams, or the
 * architects, tech writers and QA.
 * 
 * Faster go to market: Since dependencies are removed, different teams can
 * actually perform their functions at a much faster and efficient rate.
 */
